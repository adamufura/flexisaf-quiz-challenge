CREATE TABLE IF NOT EXISTS public.question (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    correct_option VARCHAR(255),
    difficulty VARCHAR(255),
    optiona VARCHAR(255),
    optionb VARCHAR(255),
    optionc VARCHAR(255),
    optiond VARCHAR(255),
    text VARCHAR(255),
    subject_id VARCHAR(255),
    CONSTRAINT fk_question_subject FOREIGN KEY (subject_id) REFERENCES public.subject (id)
);
